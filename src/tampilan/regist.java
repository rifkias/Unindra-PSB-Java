    private void bsimpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpActionPerformed
        // TODO add your handling code here:
        String sql="insert into siswa values(?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1,tid.getText());
            stat.setString(2,tnama.getText());
            
            String jkel="";
            if(rjk1.isSelected()){
                jkel="Laki-Laki";
            }else {jkel="Perempuan";
            }
            stat.setString(3, jkel);
            
            stat.setString(4, talm.getText());
            stat.setString(5, ttlp.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
            tid.requestFocus();
            datatable();
            
         }catch (SQLException e) {
             JOptionPane.showMessageDialog(null, "Data Gagal Disimpan"+e);
         
        }                                     
    }//GEN-LAST:event_bsimpActionPerformed

