    private void tabelsiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelsiswaMouseClicked
        // TODO add your handling code here:
        int bar = tabelsiswa.getSelectedRow();
        String a = tabmode.getValueAt(bar, 0).toString();
        String b = tabmode.getValueAt(bar, 1).toString();
        String c = tabmode.getValueAt(bar, 2).toString();
        String d = tabmode.getValueAt(bar, 3).toString();
        String e = tabmode.getValueAt(bar, 4).toString();
        
        tid.setText (a);
        tnama.setText (b);
        
        if(c.equals("Laki-Laki")) {rjk1.setSelected(true); rjk2.setSelected(false);}
        else {rjk1.setSelected(false); rjk2.setSelected(true);}
        
        talm.setText(d);
        ttlp.setText(e);
    }//GEN-LAST:event_tabelsiswaMouseClicked

    private void bubaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubaActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "update siswa set nama=?,jk=?,alamat=?,notelp=? where id =?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, tnama.getText());
            
            String jkel="";
            if(rjk1.isSelected()) jkel="Laki-Laki";
            else jkel="Perempuan";
            stat.setString(2, jkel);
            
            stat.setString(3, talm.getText());
            stat.setString(4, ttlp.getText());
            stat.setString(5, tid.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            kosong();
            tid.requestFocus();
            datatable();
            }catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Diubah"+e);
            
        }
    }//GEN-LAST:event_bubaActionPerformed

