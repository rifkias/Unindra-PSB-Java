    private boolean validateInput(){
        boolean res = true;
        String response = "";
        if(registerName.getText().equals("")){
            res = false;
            response += "Nama Wajib Diisi \n";
        }
        
        if(registerTempatLahir.getText().equals("")){
            res = false;
            response += "Tempat Lahir Wajib Diisi \n";
        }
        
        if(registerDob.getDate() == null){
            res = false;
            response += "Tanggal Lahir Wajib Diisi \n";
        }
        
        if(!registerJk1.isSelected() && !registerJk2.isSelected()){
            res = false;
            response += "Jenis Kelamin Wajib Dipilih \n";
        }
        
        if(registerAgama.getText().equals("")){
            res = false;
            response += "Agama Wajib Diisi \n";
        }
        
        if(!registerGolDarA.isSelected() && !registerGolDarAB.isSelected() && !registerGolDarB.isSelected() && !registerGolDarO.isSelected()){
            res = false;
            response += "Golongan Darah Wajib Dipilih \n";
        }
        
        if(registerNisn.getText().equals("")){
            res = false;
            response += "NISN Wajib Diisi \n";
        }
        
        if(registerNoTel.getText().equals("")){
            res = false;
            response += "Nomor Telpon Wajib Diisi \n";
        }
        
        if(registerNisn.getText().equals("")){
            res = false;
            response += "NISN Wajib Diisi \n";
            
            if(this.checknisn(registerNisn.getText(), selectedId)){
                res = false;
                response += "NISN sudah ada \n";
            }
        }
        
        if(registerNoTel.getText().equals("")){
            res = false;
            response += "Nomor Telpon Wajib Diisi \n";
        }
        
        if(registerEmail.getText().equals("")){
            res = false;
            response += "Email Wajib Diisi \n";
        }
        
        if(!response.equals("")){
            JOptionPane.showMessageDialog(null, response);
        }
        
        return res;
    }
    
