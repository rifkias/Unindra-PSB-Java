    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        String sql = "DELETE FROM siswa WHERE id_siswa = ?";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setInt(1, selectedId);

            int rowsAffected = stat.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                initTable();
                reset();
            } else {
                JOptionPane.showMessageDialog(null, "Data Tidak Ditemukan");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Dihapus: " + e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed
 
    private void tableSiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSiswaMouseClicked
        // TODO add your handling code here:
        int row = tableSiswa.getSelectedRow();
        
        if(row != -1){
            
            if(selectedId > 0 && selectedId == Integer.valueOf(tableSiswa.getValueAt(row, 0).toString())){
                this.reset();
            }else{
                String nama = tableSiswa.getValueAt(row, 1).toString();
                String jk = tableSiswa.getValueAt(row, 2).toString();
                String agama = tableSiswa.getValueAt(row, 3).toString();
                String golDar = tableSiswa.getValueAt(row, 4).toString();
                String tanggalLahir = tableSiswa.getValueAt(row, 5).toString();
                String tempatLahir = tableSiswa.getValueAt(row, 6).toString();
                String nisn = tableSiswa.getValueAt(row, 7).toString();
                String noTelp = tableSiswa.getValueAt(row, 8).toString();
                String email = tableSiswa.getValueAt(row, 9).toString();
                selectedId = Integer.valueOf(tableSiswa.getValueAt(row, 0).toString());

                registerName.setText(nama);
                registerAgama.setText(agama);
                registerNisn.setText(nisn);
                registerEmail.setText(email);
                registerNoTel.setText(noTelp);
                registerTempatLahir.setText(tempatLahir);
                if(jk.equals("Laki - Laki")){
                    registerJk1.setSelected(true);
                }else{
                    registerJk2.setSelected(true);
                }

                if(golDar.equals("A")){
                    registerGolDarA.setSelected(true);
                }
                if(golDar.equals("B")){
                    registerGolDarB.setSelected(true);
                }
                if(golDar.equals("AB")){
                    registerGolDarAB.setSelected(true);
                }
                if(golDar.equals("O")){
                    registerGolDarO.setSelected(true);
                }
                java.util.Date dobFormat = null;
                try{
                    dobFormat = sdf.parse(tanggalLahir);
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }

                registerDob.setDate(dobFormat);

                btnTambah.setVisible(false);
                btnUpdate.setVisible(true);
                btnHapus.setVisible(true);
            }
//            btnReset.setVisible(true);
        }
    }//GEN-LAST:event_tableSiswaMouseClicked

