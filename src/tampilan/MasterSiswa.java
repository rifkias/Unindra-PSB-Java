    public boolean checknisn(String nisn,int id){
        boolean res = true;
        String sql = "SELECT * FROM siswa WHERE nisn ='"+nisn+"'";
        if(id > 0){
            sql += "AND id_siswa != '"+id+"'";
        }
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            if(rs.next()){
                res = false;
            }
        }catch (SQLException e){
            e.printStackTrace();
        }
        
        return res;
    }
