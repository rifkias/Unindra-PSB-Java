    public boolean validateInput(){
        boolean res = true;
        String response = "";
        
        if(usernameField.getText().equals("")){
            res = false;
            response += "Username Wajib Diisi \n";
        }
        
        if(selectedId == 0 && passwordField.getText().equals("")){
            res = false;
            response += "Password Wajib Diisi \n";
        }
        
        if(namaField.getText().equals("")){
            res = false;
            response += "Nama Wajib Diisi \n";
        }
        
        if(alamatField.getText().equals("")){
            res = false;
            response += "Alamat Wajib Diisi \n";
        }
        
        if(dobField.getDate() == null){
            res = false;
            response += "Tanggal Lahir Wajib Diisi \n";
        }
        
        if(!response.equals("")){
            JOptionPane.showMessageDialog(null, response);
        }
        
        return res;
    }
    
    public void update(){
      String username = usernameField.getText();
        String password = passwordField.getText();
        String nama = namaField.getText();
        String alamat = alamatField.getText();
        java.util.Date dob = java.sql.Date.valueOf(sdf.format(dobField.getDate()));
        
        String sql = "UPDATE admin set username=?, password=?, nama=?, alamat=?, tanggal_lahir=? WHERE id_user=?";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, username);
                stat.setString(2, password);
                stat.setString(3, nama);
                stat.setString(4, alamat);
                stat.setDate(5, new java.sql.Date(dob.getTime()));
                stat.setInt(6, selectedId);

                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
                reset();
                initTable();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan: " + e.getMessage());
            }
    }
    public void insert(){
        String username = usernameField.getText();
        String password = passwordField.getText();
        String nama = namaField.getText();
        String alamat = alamatField.getText();
        java.util.Date dob = java.sql.Date.valueOf(sdf.format(dobField.getDate()));
        
        String sql = "INSERT INTO admin (username,password,nama,alamat,tanggal_lahir) VALUES (?,?,?,?,?)";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, username);
                stat.setString(2, password);
                stat.setString(3, nama);
                stat.setString(4, alamat);
                stat.setDate(5, new java.sql.Date(dob.getTime()));

                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                reset();
                initTable();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan: " + e.getMessage());
            }
    }

