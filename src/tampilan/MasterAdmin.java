    private void tableAdminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableAdminMouseClicked
        // TODO add your handling code here:
        int row = tableAdmin.getSelectedRow();
        
        
        if(row != -1){
            
            if(selectedId > 0 && selectedId == Integer.valueOf(tableAdmin.getValueAt(row, 0).toString())){
                this.reset();
            }else{
                selectedId = Integer.valueOf(tableAdmin.getValueAt(row, 0).toString());
                String username = tableAdmin.getValueAt(row, 1).toString();
                String password = tableAdmin.getValueAt(row, 2).toString();
                String nama = tableAdmin.getValueAt(row, 3).toString();
                String alamat = tableAdmin.getValueAt(row, 4).toString();
                String dob = tableAdmin.getValueAt(row, 5).toString();
                java.util.Date dates = null;
                try{
                    dates = sdf.parse(dob);
                }catch(Exception e){
                    System.err.println(e.getMessage());
                }
                usernameField.setText(username);
                passwordField.setText(password);
                namaField.setText(nama);
                alamatField.setText(alamat);
                dobField.setDate(dates);
                
                btnTambah.setVisible(false);
                btnUpdate.setVisible(true);
                btnHapus.setVisible(true);   
            }
//            btnReset.setVisible(true);
        }
        
    }//GEN-LAST:event_tableAdminMouseClicked

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        String sql = "DELETE FROM admin WHERE id_user= ?";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setInt(1, selectedId);

            int rowsAffected = stat.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                initTable();
                reset();
            } else {
                JOptionPane.showMessageDialog(null, "Data Tidak Ditemukan");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Dihapus: " + e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed
    
