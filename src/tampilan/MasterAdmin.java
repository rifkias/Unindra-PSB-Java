    public void initTable(){
        Object[] Baris={"Id","Username","Password","Nama","Alamat","Tanggal Lahir"};
        tabmode = new DefaultTableModel(null, Baris);
        tableAdmin.setModel(tabmode);
        
        String sql = "select * from admin";
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                int a = hasil.getInt("id_user");
                String b = hasil.getString("username");
                String c = hasil.getString("password");
                String d = hasil.getString("nama");
                String e = hasil.getString("alamat");
                String f = hasil.getString("tanggal_lahir");

                Object[] data={a,b,c,d,e,f};
                tabmode.addRow(data);
            }
        }catch (SQLException e){
            e.printStackTrace();
        }
    }
    
