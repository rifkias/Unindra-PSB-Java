    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String nama = registerName.getText();
        String tempatLahir = registerTempatLahir.getText();
        String agama = registerAgama.getText();
        String nisn = registerNisn.getText();
        String noTelp = registerNoTel.getText();
        String email = registerEmail.getText();
        java.util.Date dob = java.sql.Date.valueOf(sdf.format(registerDob.getDate()));
        String jenisKel = "";
        if(registerJk1.isSelected()){
            jenisKel = "Laki - Laki";
        }
        if(registerJk2.isSelected()){
            jenisKel = "Perempuan";
        }
        
        String golDar = "";
        
        if(registerGolDarA.isSelected()){
            golDar = "A";
        }
        if(registerGolDarAB.isSelected()){
            golDar =  "AB";
        }
        if(registerGolDarB.isSelected()){
            golDar = "B";
        }
        
        if(registerGolDarO.isSelected()){
            golDar = "O";
        }
        boolean checkNisn = checknisn(nisn,0);
        if(checkNisn){
            String sql = "INSERT INTO siswa (nama, jenis_kelamin, agama, golongan_darah, tanggal_lahir, nisn, tempat_lahir, nomor_telpon, email) VALUES (?,?,?,?,?,?,?,?,?)";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, nama);
                stat.setString(2, jenisKel);
                stat.setString(3, agama);
                stat.setString(4, golDar);
                stat.setDate(5, new java.sql.Date(dob.getTime()));
                stat.setString(6, nisn);
                stat.setString(7, tempatLahir);
                stat.setString(8, noTelp);
                stat.setString(9, email);

                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Ditambahkan");
                reset();
                initTable();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan: " + e.getMessage());
            }
        }else{
          JOptionPane.showMessageDialog(null, "Duplikat NISN");  
        }
    }//GEN-LAST:event_btnTambahActionPerformed
    public boolean checknisn(String nisn,int id){
        boolean res = true;
        String sql = "SELECT * FROM siswa WHERE nisn ='"+nisn+"'";
        if(id > 0){
            sql += "AND id_siswa != '"+id+"'";
        }
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            if(rs.next()){
                res = false;
            }
        }catch (SQLException e){
            e.printStackTrace();
        }
        
        return res;
    }
