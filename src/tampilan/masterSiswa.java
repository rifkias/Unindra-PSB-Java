    private void tableSiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSiswaMouseClicked
        // TODO add your handling code here:

        int row = tableSiswa.getSelectedRow();

        if(row != -1){
            selectedId = Integer.valueOf(tableSiswa.getValueAt(row, 0).toString());
            String nama = tableSiswa.getValueAt(row, 1).toString();
            String jenis_kelamin = tableSiswa.getValueAt(row, 2).toString();
            String agama = tableSiswa.getValueAt(row, 3).toString();
            String golongan_darah = tableSiswa.getValueAt(row, 4).toString();
            String tanggal_lahir = tableSiswa.getValueAt(row, 5).toString();
            String tempat_lahir = tableSiswa.getValueAt(row, 6).toString();
            String nisn = tableSiswa.getValueAt(row, 7).toString();
            String nomor_telpon = tableSiswa.getValueAt(row, 8).toString();
            String email = tableSiswa.getValueAt(row, 9).toString();

            java.util.Date reformatDate = null;
            
            try{
                reformatDate = sdf.parse(tanggal_lahir);
            }catch(Exception e){
                System.out.println(e.getMessage());
            }
            
            registerName.setText(nama);
            registerTempatLahir.setText(tempat_lahir);
            registerAgama.setText(agama);
            registerNisn.setText(nisn);
            registerNoTel.setText(nomor_telpon);
            registerEmail.setText(email);
            registerDob.setDate(reformatDate);
            if(jenis_kelamin.equals("Perempuan")){
                registerJk2.setSelected(true);
            }else{
                registerJk1.setSelected(true);
            }
            
            if(golongan_darah.equals("A")){
                registerGolDarA.setSelected(true);
            }
            if(golongan_darah.equals("AB")){
                registerGolDarAB.setSelected(true);
            }
            if(golongan_darah.equals("B")){
                registerGolDarB.setSelected(true);
            }
            if(golongan_darah.equals("O")){
                registerGolDarO.setSelected(true);
            }
            
            btnTambah.setVisible(false);
            btnUpdate.setVisible(true);
            btnHapus.setVisible(true);
            btnReset.setVisible(true);
        }
    }//GEN-LAST:event_tableSiswaMouseClicked

