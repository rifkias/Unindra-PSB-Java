    private void tableAdminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableAdminMouseClicked
        // TODO add your handling code here:
        
        int row = tableAdmin.getSelectedRow();
        
        if(row != -1){
            selectedId = Integer.valueOf(tableAdmin.getValueAt(row, 0).toString());
            String username = tableAdmin.getValueAt(row, 1).toString();
            String password = tableAdmin.getValueAt(row, 2).toString();
            String nama = tableAdmin.getValueAt(row, 3).toString();
            String alamat = tableAdmin.getValueAt(row, 4).toString();
            String dob = tableAdmin.getValueAt(row, 5).toString();
            java.util.Date dates = null;
            try{
                dates = sdf.parse(dob);
            }catch(Exception e){
                System.err.println(e.getMessage());
            }
            usernameField.setText(username);
            passwordField.setText(password);
            namaField.setText(nama);
            alamatField.setText(alamat);
            dobField.setDate(dates);
            
            btnTambah.setVisible(false);
            btnUpdate.setVisible(true);
            btnHapus.setVisible(true);
            btnReset.setVisible(true);
        }
    }//GEN-LAST:event_tableAdminMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String search = "'%"+searchTxt.getText()+"%'";
        Object[] Baris={"Id","Nama","Keterangan"};
        tabmode = new DefaultTableModel(null, Baris);
        tableAdmin.setModel(tabmode);

        String sql = "SELECT * FROM admin WHERE username LIKE " +search+" OR nama LIKE "+search+" OR alamat LIKE "+search;
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                int a = hasil.getInt("id_ekstrakulikuler");
                String b = hasil.getString("nama_esktrakulikuler");
                String c = hasil.getString("keterangan");

                Object[] data={a,b,c};
                tabmode.addRow(data);
            }
        }catch (SQLException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed
    
