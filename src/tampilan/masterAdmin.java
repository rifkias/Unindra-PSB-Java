    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        
        String username = usernameField.getText();
        String password = passwordField.getText();
        String nama = namaField.getText();
        String alamat = alamatField.getText();
        java.util.Date dob = java.sql.Date.valueOf(sdf.format(dobField.getDate()));
        
        String sql = "INSERT INTO admin (username,password,nama,alamat,tanggal_lahir) VALUES (?,?,?,?,?)";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, username);
                stat.setString(2, password);
                stat.setString(3, nama);
                stat.setString(4, alamat);
                stat.setDate(5, new java.sql.Date(dob.getTime()));

                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                reset();
                initTable();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan: " + e.getMessage());
            }
        
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:

        String username = usernameField.getText();
        String password = passwordField.getText();
        String nama = namaField.getText();
        String alamat = alamatField.getText();
        java.util.Date dob = java.sql.Date.valueOf(sdf.format(dobField.getDate()));
        
        String sql = "UPDATE admin set username=?, password=?, nama=?, alamat=?, tanggal_lahir=? WHERE id_user=?";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, username);
                stat.setString(2, password);
                stat.setString(3, nama);
                stat.setString(4, alamat);
                stat.setDate(5, new java.sql.Date(dob.getTime()));
                stat.setInt(6, selectedId);

                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
                reset();
                initTable();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan: " + e.getMessage());
            }
        
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
       String sql = "DELETE FROM admin WHERE id_user= ?";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setInt(1, selectedId);

            int rowsAffected = stat.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                initTable();
                reset();
            } else {
                JOptionPane.showMessageDialog(null, "Data Tidak Ditemukan");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Dihapus: " + e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed

