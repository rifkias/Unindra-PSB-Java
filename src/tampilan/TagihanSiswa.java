    public void getDataPembayaran(){
        Object[] Baris={"Id","Nama Tagihan","Biaya","DiBayar"};
        tabmode = new DefaultTableModel(null, Baris){
            @Override
            public boolean isCellEditable(int row, int column) {
               //all cells false
               return false;
            }
        };
        tableTagihan.setModel(tabmode);
        int totalTagihan = 0;
        int totalRow = 0;
        String sql = "select * from tagihan where id_pendaftaran = '"+pendaftaranId+"'";
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                int a = hasil.getInt("id_tagihan");
                String b = hasil.getString("nama_tagihan");
                String c = hasil.getString("biaya");
                String d = hasil.getString("dibayar");
                
                totalTagihan += (hasil.getDouble("biaya") - hasil.getDouble("dibayar"));
                Object[] data={a,b,c,d};
                tabmode.addRow(data);
            }
            
            
            this.sisaTagihan.setText("Total Tagihan : "+String.valueOf(totalTagihan));
        }catch (SQLException e){
            e.printStackTrace();
        }
    }

