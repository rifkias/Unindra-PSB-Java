    public void update(){
        String sql = "UPDATE tagihan SET dibayar=? WHERE id_tagihan=?";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, dibayarTxt.getText());
            stat.setInt(2, selectedId);

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil DiUpdate");
            getDataPembayaran();
            this.moveToTable();
            reset();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan: " + e.getMessage());
        }
    }
    
    public void checkDataPembayaran(){
        String sql = "SELECT SUM((biaya-dibayar)) as totalBiaya FROM tagihan WHERE id_pendaftaran= '"+pendaftaranId+"' AND (dibayar IS NULL OR dibayar < biaya)";
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            if(hasil.next()){
                int biaya = hasil.getInt("totalBiaya");
                if(biaya <= 0){
                    this.updateStatusPendaftaran();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Something Wrong.....");
            }
        }catch (SQLException e){
            e.printStackTrace();
        }
    }
    
    public void updateStatusPendaftaran(){
        String sql = "UPDATE PENDAFTARAN SET status = 'Validasi Pembayaran' WHERE id_pendaftaran = ?";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setInt(1, pendaftaranId);

                int rowsAffected = stat.executeUpdate();

                if (!(rowsAffected > 0)) {
                    JOptionPane.showMessageDialog(null, "Something Wrong :' ");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Dihapus: " + e.getMessage());
            }
    }
    
    public boolean validateInput(){
        boolean res = true;
        String message = "";
        
        double dibayar = 0.0;
        String dibayarString = dibayarTxt.getText();
        
        if(dibayarString.equals("")){
            message += "Field Dibayar Harus Diisi \n";
            res = false;
        }else{
            dibayar = Double.valueOf(dibayarTxt.getText());
            
            double biaya = Double.valueOf(biayaTxt.getText());
        
            if(dibayar > biaya){
                message += "Dibayar Tidak Boleh Lebih Besar Dari Biaya \n";
                res = false;
            }
        }
        
        if(!message.equals("")){
            JOptionPane.showMessageDialog(null, message);
        }
        
        return res;
    }
    
